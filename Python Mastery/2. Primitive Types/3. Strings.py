# 3 Strings

course = "Python Programming" # String
print(len(course)) # returns the lenght of the message
print(course[0]) # returns the first letter of the message 
print(course[-1]) # returns the last letter of the message

print(course[0:3]) # returns the first 3 letters of the message
print(course[0:]) # returns the whole message
print(course[:3]) # returns the first 3 letters of the message
print(course[:]) # returns the whole message

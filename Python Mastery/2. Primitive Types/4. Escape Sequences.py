coruse_3 = "Python \'Programming" # way 3
coruse_4 = "Python \\Programming" # way 4
coruse_5 = "Python \nProgramming" # way 5 \n for short means new line

print(coruse_1)
print(coruse_2)
print(coruse_3)
print(coruse_4)
print(coruse_5)